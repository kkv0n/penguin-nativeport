#ifndef OVR_228_H
#define OVR_228_H

enum OverlayRDATA_228_Counts
{
	OVR228_BUCKET_COUNT = 8,
	OVR228_SCRATCH_INIT_WORD_COUNT = 24,
	OVR228_SETUP_COPY0_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP0_WORD_COUNT,
	OVR228_SETUP_COPY1_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP1_WORD_COUNT,
	OVR228_CLIP_RECORD_JUMP_WORD_COUNT = 24,
};

#define OVR228_RDATA_START             0x800a8e38u
#define OVR228_RDATA_BUCKET_SETUP_BASE 0x800a8ed8u
#define OVR228_RDATA_STOP              0x800a9258u

enum OverlayRDATA_228_BucketSetupLayout
{
	OVR228_RDATA_BUCKET_SETUP_RECORD_STRIDE = 0x64,
	OVR228_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET = 0x1c,
	OVR228_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET = 0x58,
};

#define OVR228_RDATA_BUCKET_SETUP_ADDRESS(index)            ((u32)(OVR228_RDATA_BUCKET_SETUP_BASE + ((index) * OVR228_RDATA_BUCKET_SETUP_RECORD_STRIDE)))
#define OVR228_RDATA_BUCKET_COPY0_ADDRESS(index)            ((u32)(OVR228_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR228_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET))
#define OVR228_RDATA_BUCKET_COPY1_ADDRESS(index)            ((u32)(OVR228_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR228_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET))

#define OVR228_RETAIL_LABEL_WATER_BSP_LIST_HANDLER          0x800a10c4u
#define OVR228_RETAIL_LABEL_WATER_RENDERED_HANDLER          0x800a1b88u
#define OVR228_RETAIL_LABEL_SPLIT_GROUND_LIST_A_HANDLER     0x800a2928u
#define OVR228_RETAIL_LABEL_SPLIT_GROUND_RENDERED_A_HANDLER 0x800a37b8u
#define OVR228_RETAIL_LABEL_SPLIT_GROUND_LIST_B_HANDLER     0x800a4768u
#define OVR228_RETAIL_LABEL_SPLIT_GROUND_RENDERED_B_HANDLER 0x800a55f8u
#define OVR228_RETAIL_LABEL_WIDE_DYNAMIC_HANDLER            0x800a65a8u
#define OVR228_RETAIL_LABEL_QUAD_4X4_RENDERED_HANDLER       0x800a71fcu
#define OVR228_RETAIL_LABEL_WATER_RENDERED_DEFAULT_WRAPPER  0x800a2224u

struct OverlayRDATA_228
{
	// 0x800a8e38
	u32 bucketSetupAddresses[OVR228_BUCKET_COUNT];

	// 0x800a8e58
	u32 bucketHandlerAddresses[OVR228_BUCKET_COUNT];

	// 0x800a8e78
	u32 scratchInitTable[OVR228_SCRATCH_INIT_WORD_COUNT];

	// 0x800a8ed8
	struct DrawLevelOvrBucketSetupRecord bucketSetups[OVR228_BUCKET_COUNT];

	// 0x800a91f8
	u32 clipRecordJumpTable[OVR228_CLIP_RECORD_JUMP_WORD_COUNT];
};

extern struct OverlayRDATA_228 R228;

#endif
