#ifndef OVR_229_H
#define OVR_229_H

enum OverlayRDATA_229_Counts
{
	OVR229_BUCKET_COUNT = 8,
	OVR229_SCRATCH_INIT_WORD_COUNT = 24,
	OVR229_SETUP_COPY0_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP0_WORD_COUNT,
	OVR229_SETUP_COPY1_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP1_WORD_COUNT,
	OVR229_CLIP_RECORD_JUMP_WORD_COUNT = 24,
};

#define OVR229_RDATA_START             0x800a8eecu
#define OVR229_RDATA_BUCKET_SETUP_BASE 0x800a8f8cu
#define OVR229_RDATA_STOP              0x800a930cu

enum OverlayRDATA_229_BucketSetupLayout
{
	OVR229_RDATA_BUCKET_SETUP_RECORD_STRIDE = 0x64,
	OVR229_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET = 0x1c,
	OVR229_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET = 0x58,
};

#define OVR229_RDATA_BUCKET_SETUP_ADDRESS(index)            ((u32)(OVR229_RDATA_BUCKET_SETUP_BASE + ((index) * OVR229_RDATA_BUCKET_SETUP_RECORD_STRIDE)))
#define OVR229_RDATA_BUCKET_COPY0_ADDRESS(index)            ((u32)(OVR229_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR229_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET))
#define OVR229_RDATA_BUCKET_COPY1_ADDRESS(index)            ((u32)(OVR229_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR229_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET))

#define OVR229_RETAIL_LABEL_WATER_BSP_LIST_HANDLER          0x800a1178u
#define OVR229_RETAIL_LABEL_WATER_RENDERED_HANDLER          0x800a1c3cu
#define OVR229_RETAIL_LABEL_SPLIT_GROUND_LIST_A_HANDLER     0x800a29dcu
#define OVR229_RETAIL_LABEL_SPLIT_GROUND_RENDERED_A_HANDLER 0x800a386cu
#define OVR229_RETAIL_LABEL_SPLIT_GROUND_LIST_B_HANDLER     0x800a481cu
#define OVR229_RETAIL_LABEL_SPLIT_GROUND_RENDERED_B_HANDLER 0x800a56acu
#define OVR229_RETAIL_LABEL_WIDE_DYNAMIC_HANDLER            0x800a665cu
#define OVR229_RETAIL_LABEL_QUAD_4X4_RENDERED_HANDLER       0x800a72b0u
#define OVR229_RETAIL_LABEL_WATER_RENDERED_DEFAULT_WRAPPER  0x800a22d8u

struct OverlayRDATA_229
{
	// 0x800a8eec
	u32 bucketSetupAddresses[OVR229_BUCKET_COUNT];

	// 0x800a8f0c
	u32 bucketHandlerAddresses[OVR229_BUCKET_COUNT];

	// 0x800a8f2c
	u32 scratchInitTable[OVR229_SCRATCH_INIT_WORD_COUNT];

	// 0x800a8f8c
	struct DrawLevelOvrBucketSetupRecord bucketSetups[OVR229_BUCKET_COUNT];

	// 0x800a92ac
	u32 clipRecordJumpTable[OVR229_CLIP_RECORD_JUMP_WORD_COUNT];
};

extern struct OverlayRDATA_229 R229;

#endif
