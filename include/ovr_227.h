#ifndef OVR_227_H
#define OVR_227_H

enum OverlayRDATA_227_Counts
{
	OVR227_BUCKET_COUNT = 11,
	OVR227_SCRATCH_INIT_WORD_COUNT = 24,
	OVR227_SETUP_COPY0_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP0_WORD_COUNT,
	OVR227_SETUP_COPY1_WORD_COUNT = DRAW_LEVEL_OVR_COPIED_SETUP1_WORD_COUNT,
	OVR227_CLIP_RECORD_JUMP_WORD_COUNT = 24,
};

#define OVR227_RDATA_START             0x800ab48cu
#define OVR227_RDATA_BUCKET_SETUP_BASE 0x800ab544u
#define OVR227_RDATA_STOP              0x800ab9f0u

enum OverlayRDATA_227_BucketSetupLayout
{
	OVR227_RDATA_BUCKET_SETUP_RECORD_STRIDE = 0x64,
	OVR227_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET = 0x1c,
	OVR227_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET = 0x58,
};

#define OVR227_RDATA_BUCKET_SETUP_ADDRESS(index)        ((u32)(OVR227_RDATA_BUCKET_SETUP_BASE + ((index) * OVR227_RDATA_BUCKET_SETUP_RECORD_STRIDE)))
#define OVR227_RDATA_BUCKET_COPY0_ADDRESS(index)        ((u32)(OVR227_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR227_RDATA_BUCKET_SETUP_COPY0_ADDRESS_OFFSET))
#define OVR227_RDATA_BUCKET_COPY1_ADDRESS(index)        ((u32)(OVR227_RDATA_BUCKET_SETUP_ADDRESS(index) + OVR227_RDATA_BUCKET_SETUP_COPY1_ADDRESS_OFFSET))

#define OVR227_RETAIL_LABEL_FULL_DYNAMIC_HANDLER        0x800a1010u
#define OVR227_RETAIL_LABEL_WATER_BSP_LIST_HANDLER      0x800a1f34u
#define OVR227_RETAIL_LABEL_WATER_RENDERED_HANDLER      0x800a29f8u
#define OVR227_RETAIL_LABEL_GROUND_4X1_LIST_HANDLER     0x800a3798u
#define OVR227_RETAIL_LABEL_GROUND_4X1_RENDERED_HANDLER 0x800a4258u
#define OVR227_RETAIL_LABEL_GROUND_4X2_LIST_HANDLER     0x800a5074u
#define OVR227_RETAIL_LABEL_GROUND_4X2_RENDERED_HANDLER 0x800a5f1cu
#define OVR227_RETAIL_LABEL_DYNAMIC_LIST_HANDLER        0x800a6ff8u
#define OVR227_RETAIL_LABEL_DYNAMIC_RENDERED_HANDLER    0x800a7c4cu
#define OVR227_RETAIL_LABEL_QUAD_4X4_LIST_HANDLER       0x800a8bfcu
#define OVR227_RETAIL_LABEL_QUAD_4X4_RENDERED_HANDLER   0x800a9850u

struct OverlayRDATA_227
{
	// 0x800ab48c
	u32 bucketSetupAddresses[OVR227_BUCKET_COUNT];

	// 0x800ab4b8
	u32 bucketHandlerAddresses[OVR227_BUCKET_COUNT];

	// 0x800ab4e4
	u32 scratchInitTable[OVR227_SCRATCH_INIT_WORD_COUNT];

	// 0x800ab544
	struct DrawLevelOvrBucketSetupRecord bucketSetups[OVR227_BUCKET_COUNT];

	// 0x800ab990
	u32 clipRecordJumpTable[OVR227_CLIP_RECORD_JUMP_WORD_COUNT];
};

extern struct OverlayRDATA_227 R227;

#endif
